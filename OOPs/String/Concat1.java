public class Concat1 {
    public static void main(String[] args) {
        
        String first = "Md";
        String sec = "Shahnawaz";
        System.out.println(first);
        System.out.println(sec);

        // to get concatenation of two string
        String sc = first.concat(sec);
        System.out.println("Concatenation = "+sc);
    }
    
}
